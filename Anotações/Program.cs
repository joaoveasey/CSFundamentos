//gerando uma IL (intermediate language ou bytecode) que gera um arquivo .exe ou .dll

//EXECUÇÃO:
//Just in Time Compilador: traduz o código de máquina no momento em que ele vai ser executado
//usando a Virtual Machine do ambiente utilizado (Windows, MacOs, Linux)

//BCL(Biblioteca de classes base): o.NET Framework oferece uma biblioteca de códigos testados e reutilizáveis 
//que devs podem chamar de seus próprios aplicativos.

//.NET Standard: unifica o .NET CORE e o .NET FRAMEWORK

